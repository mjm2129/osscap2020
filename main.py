def LED_init():
    thread=threading.Thread(target=LED.main, args=())
    thread.setDaemon(True)
    thread.start()
    return

total_card = [[[0 for x in range(16)] for x in range(8)] for x in range(56)]
def initcard():
    global total_card
    c=0
    for p in range(4):
        for i in range(14):
            for j in range(8):
                for k in range(16):
                    if card[i][j][k] != 0:
                        total_card[c][j][k] = card[i][j][k] + p
            c+=1

LED_init()
initcard()
random.shuffle(total_card)
